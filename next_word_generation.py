corpus = text.lower().split('\n')

tokenizer = Tokenizer()
tokenizer.fit_on_texts(corpus)
total_words = len(tokenizer.word_index) +1


input_sequence = []
for i in  corpus:
  seq = tokenizer.texts_to_sequences([i])[0]
  # print(i)
  for i in range(1,len(seq)):
    n_gram_seq = seq[:i+1]
    input_sequence.append(n_gram_seq)

max_length = np.max([len(x) for x in input_sequence])

input_sequence = np.array(pad_sequences(input_sequence,maxlen=max_length,padding='pre'))
# input_sequence = np.array(pad_sequences(input_sequence, maxlen=max_length, padding='pre'))
X = input_sequence[:,:-1]
labels = input_sequence[:,-1]
y = tf.keras.utils.to_categorical(labels,num_classes = total_words)

def model():
  model = Sequential()
  model.add(Embedding(total_words,64,input_length= max_length -1))
  model.add(Bidirectional(LSTM(20)))
  model.add(Dense(total_words,activation='softmax'))
  model.compile(loss='categorical_crossentropy',optimizer='adam',metrics=['acc'])
  return model

model = model()

model.summary()

h = model.fit(X,y,epochs=500,verbose=1)

